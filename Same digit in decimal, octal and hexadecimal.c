#include <stdio.h>

int main(){

    int a = 010;

    // Any number starting with 0 represents octal Ex: 010
    // Any number starting with 0x represents hexadecimal Ex: 0x10A


    printf("Decimal = %d\n", a); //Decimal Identifier

    printf("Octal = %o\n", a);   //Octal Identifier

    printf("Hexadecimal = %x\n", a); //Hexadecimal Identifier

    return 0;

}
